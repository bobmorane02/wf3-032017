// Sélecteur de balise HTML (tag)

var myParaTag = document.getElementsByTagName('p');
console.log(myParaTag);

// Sélecteur de class

var myParaClass = document.getElementsByClassName('myClass');
console.log(myParaClass);

// Sélecteur d'id

var myParaID = document.getElementById('myId');
console.log(myParaID);

// Sélecteur querySelector
console.log(document.querySelector('p'));

// Sélecteur querySélectorAll
console.log(document.querySelectorAll('.myClass'));